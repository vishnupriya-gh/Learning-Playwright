
let a = 10, b = 3;
Addition = a + b   // 13 (Addition)
Subtraction = a - b   // 7  (Subtraction)
Multiplication = a * b   // 30 (Multiplication)
Division = a / b   // 3.33 (Division)
Modulus = a % b   // 1  (Modulus - remainder)
Exponentiation = a ** b  // 1000 (Exponentiation)
Increment = a++     // 11 (Increment)
Decrement = b--     // 2  (Decrement)
console.log("Addition:",Addition);
console.log("Subtraction:",Subtraction);
console.log("Multiplication:",Multiplication);
console.log("Division:",Division);  
console.log("Modulus:",Modulus);
console.log("Exponentiation:",Exponentiation);
console.log("Increment:",Increment);
console.log("Decrement:",Decrement);